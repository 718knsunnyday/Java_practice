public class Calculation {
  public static void main(String[] args){
    double a = 9.0/2;
    System.out.println(a);
    int b = 1000;
    b++ ;
    System.out.println(b);
    int c = 1000;
    c-- ;
    System.out.println(c);
    b += 10;
    System.out.println(b);
    double e = 8.5*2;
    System.out.println(e);
    double f = 8.5/2;
    System.out.println(f);
    double g = 7;
    System.out.println(g);
  }
}